package lineasTelefonicas;

public class Linea {

	private String numero;

	public Linea(String numero) {
		this.numero = numero;
	}

	/**
	 * @return the numero
	 */
	public String getNumero() {
		return numero;
	}

	/**
	 * @param numero the numero to set
	 */
	public void setNumero(String numero) {
		this.numero = numero;
	}

}
